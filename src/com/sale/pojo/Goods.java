package com.sale.pojo;

public class Goods {
	private int g_id;
	private String g_name;
	private String g_desc;
	private double g_price;
	private int g_count;
	private String g_number;
	private String g_intime;
	private int g_status;
	private double g_inprice;
	public int getG_id() {
		return g_id;
	}
	public void setG_id(int g_id) {
		this.g_id = g_id;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_desc() {
		return g_desc;
	}
	public void setG_desc(String g_desc) {
		this.g_desc = g_desc;
	}
	public double getG_price() {
		return g_price;
	}
	public void setG_price(double g_price) {
		this.g_price = g_price;
	}
	public int getG_count() {
		return g_count;
	}
	public void setG_count(int g_count) {
		this.g_count = g_count;
	}
	public String getG_number() {
		return g_number;
	}
	public void setG_number(String g_number) {
		this.g_number = g_number;
	}
	public String getG_intime() {
		return g_intime;
	}
	public void setG_intime(String g_intime) {
		this.g_intime = g_intime;
	}
	public int getG_status() {
		return g_status;
	}
	public void setG_status(int g_status) {
		this.g_status = g_status;
	}
	public double getG_inprice() {
		return g_inprice;
	}
	public void setG_inprice(double g_inprice) {
		this.g_inprice = g_inprice;
	}
}
